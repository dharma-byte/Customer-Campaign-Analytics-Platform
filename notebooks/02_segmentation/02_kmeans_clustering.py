# %% [markdown]
# ## 4 — Silhouette Plot (Cluster Cohesion Validation)

# %%
sil_sample = silhouette_samples(X, cluster_labels)
fig, ax = plt.subplots(figsize=(10, 7))
y_lower = 10

for cl in range(optimal_k):
    cl_sil = np.sort(sil_sample[cluster_labels == cl])
    cl_size = cl_sil.shape[0]
    y_upper = y_lower + cl_size

    ax.fill_betweenx(
        np.arange(y_lower, y_upper), 0, cl_sil,
        facecolor=CLUSTER_COLORS[cl], edgecolor="white", alpha=0.8,
    )
    ax.text(-0.02, y_lower + 0.5 * cl_size, f"C{cl}",
            ha="right", va="center", fontsize=10, fontweight="bold",
            color=CLUSTER_COLORS[cl])
    y_lower = y_upper + 10

ax.axvline(sil_final, color=C["red"], linestyle="--", linewidth=2,
           label=f"Mean silhouette = {sil_final:.3f}")
ax.set_title("Silhouette Plot — Cluster Cohesion Analysis")
ax.set_xlabel("Silhouette Coefficient")
ax.set_ylabel("Cluster")
ax.set_yticks([])
ax.set_xlim(-0.1, 1.0)
ax.legend(fontsize=10)

plt.tight_layout()
plt.savefig(EXPORTS / "kmeans_silhouette.png", bbox_inches="tight", dpi=150)
