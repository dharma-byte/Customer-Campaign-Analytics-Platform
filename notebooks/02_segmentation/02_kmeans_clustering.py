rfm_scores  = pd.read_csv(PROCESSED / "rfm_scores.csv").set_index("customer_id")

print(f"Loaded feature matrix : {feat_scaled.shape[0]:,} customers x {feat_scaled.shape[1]} features")
print(f"Scaled features used  : {feat_scaled.columns.tolist()}")

X = feat_scaled.values

# %% [markdown]
# ## 1 — Elbow Method (Optimal K Selection)

# %%
K_RANGE = range(2, 11)
inertias, sil_scores = [], []

print("Computing inertia and silhouette scores for K = 2 … 10 ...")
for k in K_RANGE:
    km = KMeans(n_clusters=k, random_state=42, n_init=10, max_iter=300)
    labels = km.fit_predict(X)
    inertias.append(km.inertia_)
    sil_scores.append(silhouette_score(X, labels, sample_size=2000, random_state=42))
    print(f"  K={k}: inertia={km.inertia_:>12,.0f}  silhouette={sil_scores[-1]:.4f}")

fig, axes = plt.subplots(1, 2, figsize=(14, 5))
fig.suptitle("K-Means: Optimal Cluster Count Selection", fontsize=13, fontweight="bold")

# ── Elbow curve ─────────────────────────────────────────────────────────────
ax = axes[0]
ax.plot(list(K_RANGE), inertias, marker="o", color=C["navy"],
        linewidth=2, markersize=8)
ax.fill_between(list(K_RANGE), inertias, alpha=0.1, color=C["navy"])
ax.axvline(5, color=C["red"], linestyle="--", linewidth=2, label="Selected K=5")
ax.set_title("Elbow Method — Within-Cluster Inertia")
ax.set_xlabel("Number of Clusters (K)")
ax.set_ylabel("Inertia (Sum of Squared Distances)")
ax.legend(fontsize=10)
ax.xaxis.set_major_locator(mticker.MaxNLocator(integer=True))

# ── Silhouette score ─────────────────────────────────────────────────────────
ax = axes[1]
bars = ax.bar(list(K_RANGE), sil_scores,
              color=[C["red"] if k == 5 else C["teal"] for k in K_RANGE],
              edgecolor="white")
ax.set_title("Silhouette Score (higher = better-defined clusters)")
ax.set_xlabel("Number of Clusters (K)")
ax.set_ylabel("Silhouette Score")
ax.xaxis.set_major_locator(mticker.MaxNLocator(integer=True))
for bar, v in zip(bars, sil_scores):
    ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.001,
            f"{v:.3f}", ha="center", va="bottom", fontsize=9,
            fontweight="bold" if v == max(sil_scores) else "normal")

plt.tight_layout()
plt.savefig(EXPORTS / "kmeans_optimal_k.png", bbox_inches="tight", dpi=150)
plt.show()

optimal_k = 5
print(f"\nSelected K = {optimal_k}")
print(f"Rationale: K=5 aligns with HSBC's operational segment model (Mass Market,")
print(f"           Affluent, Premier, Private Banking + emerging cross-sell tier).")
print(f"           Silhouette at K=5: {sil_scores[optimal_k-2]:.4f}")

# %% [markdown]
# ## 2 — Fit Final K-Means Model (K=5)

# %%
np.random.seed(42)
km_final = KMeans(n_clusters=optimal_k, random_state=42,
                  n_init=20, max_iter=500)
cluster_labels = km_final.fit_predict(X)

feat_raw["cluster"]    = cluster_labels
feat_scaled["cluster"] = cluster_labels

# Merge in RFM segment for cross-tab
feat_raw["rfm_segment"] = rfm_scores["rfm_segment"]

sil_final = silhouette_score(X, cluster_labels)
print(f"Final model silhouette score : {sil_final:.4f}")
print(f"Cluster sizes:")
for cl, count in pd.Series(cluster_labels).value_counts().sort_index().items():
    print(f"  Cluster {cl} : {count:,} customers ({count/len(cluster_labels)*100:.1f}%)")

