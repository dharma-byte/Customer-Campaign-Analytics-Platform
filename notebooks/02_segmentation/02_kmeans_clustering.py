# %% [markdown]
# # Notebook 2 — K-Means Customer Clustering
# **Customer Campaign Analytics Platform | CCAP**
#
# **Why K-Means on top of RFM?**
# RFM segments customers on three pre-defined axes. K-Means discovers
# *natural groupings* across all 19 features simultaneously — including
# income, credit score, product holdings, and engagement patterns that
# RFM alone cannot capture.
#
# The resulting clusters give the CRM team behavioural archetypes like
# "young high-earners with no products yet" or "mature multi-product
# customers with declining engagement" — segments that a rule-based
# system would miss entirely.
#
# **Approach:**
# 1. Elbow method to identify optimal K
# 2. Silhouette analysis to validate cluster tightness
# 3. K-Means with K=5 (matching HSBC's operational segment count)
# 4. PCA 2D biplot for visual validation
# 5. Cluster profiling — who is in each cluster?
# 6. Cross-tabulation with RFM segments
# 7. Business interpretation and campaign action per cluster

# %%
